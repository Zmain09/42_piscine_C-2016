char	*ft_strncat(char *dest, char *src, int nb)
{
	int i;
	int i2;

	i = 0;
	i2 = 0;
	while (dest[i2])
		i2++;
	while (i < nb && src[i])
	{
		dest[i2] = src[i];
		i++;
		i2++;
	}
	dest[i2] = '\0';
	return (dest);
}
